console.log("Operators in JS")

let a = 45;
let b = 4;

console.log("a + b = ", a+b)
console.log("a - b = ", a-b)
console.log("a * b = ", a*b)
console.log("a / b = ", a/b)
console.log("a ** b = ", a**b) //a to the power b
console.log("a % b = ", a%b) //remainder

console.log("a++ : ",a++) //first a will be printed and then the increment is performed
console.log("a = ",a)
console.log("a-- : ",a--) //The same case as before


