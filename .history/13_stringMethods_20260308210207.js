let name = "Alice"
console.log(name.length) // to find the length of the string

console.log(name.toUpperCase()) // to convert the string to uppercase

console.log(name.slice(2,4)) // to slice the string from index 2 to 4 (not including 4)

console.log(name.slice(2)) // to slice the string from index 2 to the end of the string

console.log(name.replace("Ali","pri")) // to replace a part of the string with another string

let friend = 'Bob'
console.log(name.concat(" is a friend of ", friend)) // to concatenate two strings

let friend2 = "         Charlie     "
console.log(friend2) // to print the string with whitespace
console.log(friend2.trim()) // to remove whitespace from both ends of the string


